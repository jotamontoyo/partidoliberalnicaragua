<h2 class="pb-2 centrado"><img src="../public/img/logos/logoPLN.jpg" style="max-width: 40px;" alt="">Metodología a seguir para la afiliación, mecanismos, selección, desempeño y controles de la organización</h2>
<hr>
<br>


<ul><h4 class="centrado"><img src="../public/img/logos/metodologiaPicar.png" class="img_titulos_metodologia" alt="">[M] Metodología PICAR</h4>
    <h6><b>Destinada a establecer los alineamientos, acciones y productos para la lograr una real y efectiva estructuración, posicionamiento y reconocimiento local como del exterior y contar con el apoyo internacional del nuevo Partido Liberal de Nicaragua como la principal fuerza de oposición no Sandinista que será la base para ganar las elecciones cuando existan condiciones.</b></h6>
</ul>
<br>


<ul><h4 class="centrado"><img src="../public/img/logos/procedimientoPicar.png" class="img_titulos_metodologia" alt="">[P] Procedimiento</h4>
    <h6><b>Estructura de alineamientos, acciones y participación de la dirección como de la afiliación en todos sus niveles.</b></h6>
    <li><b>a)</b> Creación de documentos sobre los lineamientos generales según competencias.</li>
    <li><b>b)</b> Criterios y procedimientos para la elección de miembros del comité ejecutivo.</li>
    <li><b>c)</b> Criterios de selección para la elección de representantes territoriales.</li>
    <li><b>d)</b> Requerimientos base para la afiliación.</li>
    <li><b>e)</b> Métodos para la afiliación territorial y general.</li>
    <li><b>f)</b> Selección de integrantes a comisiones.
        <ul>
            <li><b>f.1)</b> Planificación: Acciones/ Monitoreo, seguimiento del plan de acción.</li>
            <li><b>f.2)</b> Relaciones Exteriores: Enlace con organismos y personalidades.</li>
            <li><b>f.3)</b> Comunicación: divulgación y relación con los medios.</li>
            <li><b>f.4)</b> Afiliación y coordinación: Registro de estructuras y comunicación.</li>
            <li><b>f.5)</b> Seguimiento y Control: de tareas según estructura funcional.</li>
            <li><b>f.6)</b> Auditoria: Supervisión de ingresos y ejecución del gasto.</li>
        </ul>
    </li>
    <li><b>g)</b> Modelo de enunciados, opciones y evaluación de encuestas según niveles de participación, afiliados, representantes territoriales y abiertas a la población.</li>
    <li><b>h)</b> Proceso de inclusión de temas a tratar en comisiones y modalidad de reporte de avances.</li>
    <li><b>i)</b> Normas, contenidos, cronogramas de reuniones en los distintos niveles, de dirección como para las representaciones territoriales.</li>
    <li><b>j)</b> Modelos de actas, comunicados y notas de prensa</li>
    <li><b>k)</b> Guías para la redacción y presentación de documentos a lo interno como hacía el exterior./li>
    <li><b>l)</b> Composición del modelo de informes internos a nivel de comité ejecutivo como de representaciones territoriales.</li>
    <li><b>m)</b> Procedimiento, valoración y toma de decisiones para lograr alianzas con sectores, organizaciones y otros.</li>
</ul>
<br>



<ul><h4 class="centrado"><img src="../public/img/logos/instrumentosPicar.png" class="img_titulos_metodologia" alt="">[I] Instrumentos</h4>
    <h6><b>Estrategias secuenciales y paralelas apoyadas por herramientas tecnológicas de acuerdo a un plan lógico de implementación con la participación de los representantes territoriales y afiliados en general.</b></h6>
    <li><b>a)</b> Hoja de afiliación digital.</li>
    <li><b>b)</b> Afiliación vía WhatsApp al teléfono del partido con registro en base de datos por asignar.</li>
    <li><b>c)</b> Utilización del padrón para verificación y complementación de información de afiliados para registro en Base de Datos central.</li>
    <li><b>d)</b> Base de Datos central de registro de afiliados.</li>
    <li><b>e)</b> Base de datos de representantes territoriales y miembros de comisiones.</li>
    <li><b>f)</b> Base de datos con registros de organismos, autoridades, medios de comunicación y personalidades.</li>
    <li><b>g)</b> Página Web divulgativa, con registro de afiliados y encuestas.</li>
    <li><b>h)</b> Página en Facebook informativa con link directo a la página Web.</li>
    <li><b>i)</b> Cuenta en Zoom multi usuarios para uso de la dirección como de las representaciones territoriales.</li>
    <li><b>j)</b> Cuentas es Instagram y en X.</li>
    <li><b>k)</b> Metodología de identificación de nicaragüenses en Lidkelin y en Facebook para envío de información, seguimiento para afiliar a los identificados.</li>
    <li><b>l)</b> Mecanismo de encuestas por WhatsApp y a incluir en página Web y Facebook.</li>
    <li><b>m)</b> Mecanismo de tabulación de resultados de encuestas para toma de decisiones.</li>
    <li><b>n)</b> Generación de grupos WhatsApp, emails para comunicación a grupos específicos según acciones a realizar.</li>
    <li><b>o)</b> Solicitud y registro de firma digital a afiliados para apoyar un documento para respaldar iniciativas o para enviar a autoridades que los representan.</li>
    <li><b>p)</b> Empadronamiento (ver información del proyecto).</li>
</ul>
<br>



<ul><h4 class="centrado"><img src="../public/img/logos/controlPicar.png" class="img_titulos_metodologia" alt="">[C] Control</h4>
    <h6><b>Asignación de actores a las distintas acciones y seguimiento del cumplimiento previsto.</b></h6>
    <li>a) Control de Representaciones territoriales según el avance de inclusión de miembros; En Nicaragua serán los 153 municipios con 5 miembros por cada municipio, 15 departamentos con 7 miembros y 2 regiones autónomas con 7 miembros. En el exterior 5 miembros por país y 3 por cada ciudad o estado.</li>
    <li>b) Control de afiliación registrada desde las distintas representaciones tanto en Nicaragua como en el exterior.</li>
    <li>c) Validación de la información de registros de afiliados; datos generales Cédula o Identificación y foto del documento tanto de las representaciones como de afiliados.</li>
    <li>d) Control de divulgación a las representaciones territoriales de los temas abordados en el comité ejecutivo.</li>
    <li>e) Control de divulgación a la afiliación en general, ya sea desde casa representación territorial o desde nivel central comité ejecutivo.</li>
    <li>f) Control y registro de sesiones según agenda del comité ejecutivo.</li>
    <li>g) Recibo de agendas y actas de sesiones de las representaciones territoriales.</li>
    <li>h) Control y programación de asignaciones de sesiones de zoom.</li>
    <li>i) Revisión de actas de las representaciones para ser abordadas por el comité ejecutivo y respuestas de los resuelto.</li>
    <li>j) Control de contenidos a divulgar a medios y redes sociales.</li>
    <li>k) Recepción, análisis y ejecución de solicitudes de baja de representantes territoriales o afiliados.</li>
    <li>l) Seguimiento de publicaciones donde hacen referencia al partido.</li>
</ul>
<br>



<ul><h4 class="centrado"><img src="../public/img/logos/auditoriaPicar.png" class="img_titulos_metodologia" alt="">[A] auditoría</h4>
    <h6><b>De certificación por organismos externos sobre la veracidad de la existencia de la organización como de nuestra presencia con representación territorial.</b></h6>
    <li><b>a)</b> Preparación de listas de representaciones territoriales para la comprobación aleatoria de la veracidad de existencia.</li>
    <li><b>b)</b> Evaluación continúa de las tareas de validación por entidades y organismos externos que realizaron verificación.</li>
    <li><b>c)</b> Reemplazo o inclusión de registros fallidos producto de la validación externa.</li>
    <li><b>d)</b> Auditoría sobre el cumplimiento del seguimiento de lo abordado en sesión de las distintas representaciones.</li>
    <li><b>e)</b> Auditoría sobre el cumplimiento en medios según áreas geográficas de las representaciones.</li>
    <li><b>f)</b> Recuento y detalle de aportaciones periódicas o extraordinarias de los afiliados por representaciones o general.</li>
</ul>
<br>


<ul><h4 class="centrado"><img src="../public/img/logos/resultadosPicar.png" class="img_titulos_metodologia" alt="">[R] resultados</h4>
    <h6><b>Lograr tener representantes territoriales en esta etapa de forma clandestina en todos los municipios en Nicaragua, en los países y ciudades donde residen nicaragüenses como; Estados Unidos, Costa Rica, Canadá, España y resto de países de CA, Latinoamérica como en Europa.</b></h6>
    <li>a) Contar con procesos de consulta con nuestras bases totalmente transparentes, utilizando herramientas tecnológicas para la elección de miembros de la junta directiva del partido, comisiones y resto de posiciones relevantes.</li>
    <li>b) Con el proyecto del EMPADRONAMIENTO lograr la participación de esos más de 1.5 millones de la diáspora como de los recientes exilios.</li>
    <li>c) Ser reconocidos internacionalmente, logrando apoyos económicos para proyectos específicos como para el funcionamiento del partido en sí.</li>
    <li>d) Aportes de la afiliación vía aportes periódicos.</li>
    <li>e) Ganarnos el puesto para que obligatoriamente debido al volumen de afiliación a alcanzar como por el reconocimiento internacional para ser incluidos en cualquier tipo de negociación con los actores principales (países) como en diálogos con el régimen a sucederse en el futuro.</li>
    <li>f) Estar preparados para cuando sea el momento tener la capacidad de cubrir todas las posiciones de un partido ya en Nicaragua, depuración del padrón, tendido electoral y resto de acciones necesarias.</li>
    <li>g) Organización y celebración de primarias para elegir a las autoridades de Cargos de elección.</li>
</ul>
